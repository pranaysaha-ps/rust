fn main() {
    // Implicitly defined integers
    let x = 10;

    //Explicitly defined integers
    let a:i32 = 10;
    let b:u64 = 20;
    let c:usize = 30;
    let d:isize = 40;

    //print the values
    println!("x: {}", x);
    println!("a: {}", a);
    println!("b: {}", b);
    println!("c: {}", c);
    println!("d: {}", d);
}